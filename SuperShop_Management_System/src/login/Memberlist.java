package login;

/**
 *
 * @author MN
 */
public class Memberlist {
    int cid;
    String name,phone,address;
    int point;

    public Memberlist(int cid, String name, String phone, String address, int point) {
        this.cid = cid;
        this.name = name;
        this.phone = phone;
        this.address = address;
        this.point = point;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
    
}
